    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!(this.jTextField2.getText()).equals("")||!this.namedb.getText().equals("")){
             String host=this.host.getText(),
                        user=this.user.getText(),
                        pass=this.pass.getText(),
                        namedb=this.namedb.getText(),sql="";
            if(this.t==0){ // abrir :D
                if(this.jRadioButton1.isSelected())
                    sql="SELECT * FROM "+this.jTextField2.getText()+"";
                else
                    sql=this.jTextField2.getText();
                try {
                ConectMysql o=new  ConectMysql(host,user,pass,namedb);
                DefaultTableModel variables = o.getValues(sql);
                int nvariables = variables.getColumnCount() - 1;
                int nobjetos = variables.getRowCount();
                 Thread open=new Thread(){
                public void run(){
                new Observacional(variables, nobjetos, nvariables).setVisible(true);
                this.stop();
                 }
                };
                open.start();
                o.closeConnection();
                this.dispose();
                } catch (Exception ex) {
                Logger.getLogger(Mysqindex.class.getName()).log(Level.SEVERE, null, ex);
                 JOptionPane.showMessageDialog(null, "Error Verifique sus datos");
            }
            }
            else{
                try {
                // gurdr
                sql=this.jTextField2.getText();
                ConectMysql o=new  ConectMysql(host,user,pass,namedb); // Conexion
                
                Value a=(Value) this.mx.get(0);
               o.createTable(sql, a.getValue().size());// crea tabla
               for(int x=0;x<mx.size();x++){
                   Value a1=(Value) this.mx.get(x);
               o.insertData(sql,a1.getValue());
               }
               o.closeConnection();
               this.dispose();
               
               
               
                System.out.println("creado table");
                 } catch (Exception ex) {
                     Logger.getLogger(Mysqindex.class.getName()).log(Level.SEVERE, null, ex);
                      JOptionPane.showMessageDialog(null, "Error Verifique sus datos");
                 }
                
                
                
            
            }
        
        }
        else {
         JOptionPane.showMessageDialog(null, "Falta informci√≥n,Verifique sus datos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

