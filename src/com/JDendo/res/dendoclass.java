public class dendoclass {
    private String a="";
    private String b="";
    private double seme=0.0;
    public dendoclass(String a1,String b1,double s){
        this.a=a1;
        this.b=b1;
        this.seme=s;
    }

    /**
     * @return the a
     */
    public String getA() {
        return a;
    }

    /**
     * @return the b
     */
    public String getB() {
        return b;
    }

    /**
     * @return the seme
     */
    public double getSeme() {
        return seme;
    }
    
    
}
