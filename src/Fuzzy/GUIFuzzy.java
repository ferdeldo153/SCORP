 private DefaultTableModel addvalue(int p) {
      DefaultTableModel  variables = new DefaultTableModel();
        variables.addColumn("Nombre");
        for (int x = 1; x < captura.size(); x++) {
            variables.addColumn(captura.get(x));
        }
        variables.addColumn("*"+JOptionPane.showInputDialog("¿Nombre de la nueva variable?"));
        String[] Dato = new String[captura.size()+1];
        for (int x = 0; x < this.m.size(); x++) {
            Value v = (Value) m.get(x);
            Dato[0] = v.getName();
            for (int y = 0; y < v.getValue().size(); y++) {
                    Dato[y + 1] = (String) v.getValue().get(y);
                }
             if(this.tipo==1)
                        Dato[v.getValue().size()+1] = ""+triangular(Double.parseDouble(Dato[p]));
             if(this.tipo==2)
                        Dato[v.getValue().size()+1] = ""+trapezoidal(Double.parseDouble(Dato[p]));
             if(this.tipo==3)
                        Dato[v.getValue().size()+1] = ""+bell(Double.parseDouble(Dato[p]));
             if(this.tipo==4)
                     Dato[v.getValue().size()+1] = ""+gauss(Double.parseDouble(Dato[p]));
            if(this.tipo==5)
                     Dato[v.getValue().size()+1] = ""+sig(Double.parseDouble(Dato[p]));
            variables.addRow(Dato);
        }
        return variables;
    }
