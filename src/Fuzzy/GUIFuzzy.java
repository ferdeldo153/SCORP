private double triangular(double value){
    double r=0.0;
    if(value<=this.a||value>this.c)
        r=0.0;
    if(this.a<=value&&value<this.b)
        r=(value-this.a)/(this.b-this.a);
    if(this.b<=value&&value<this.c)
         r=(this.c-value)/(this.c-this.b);
    
    return r;
}
private double trapezoidal(double value){
    double r=0.0;
    if(value<=this.a||value>this.d)
        r=0.0;
    if(this.a<=value&&value<this.b)
        r=(value-this.a)/(this.b-this.a);
    if(this.b<=value&&value<this.c)
         r=1;
     if(this.c<=value&&value<this.d)
        r=(this.d-value)/(this.d-this.c);
    
    return r;
}
private double bell(double value){
    double r=1/(1+Math.pow(Math.abs((value-c)/a),2*b));
    return r;
}
private double gauss(double value){
    double r=Math.pow(Math.E,-0.5*Math.pow((value-c)/a,2));
    return r;
}
private double sig(double value){
    double r=0;
     if(value<=this.a)
         r=0;
     if(this.a<=value&&value<this.b)
         r=2*Math.pow((value-this.a)/(this.c-this.a),2);
      if(this.b<=value&&value<this.c)
          r=1-(2*Math.pow((value-this.c)/(this.c-this.a),2));
     if(value>this.c)
         r=1;
    // return 1.0 / (1 + Math.exp(-value));
    return r;
}
