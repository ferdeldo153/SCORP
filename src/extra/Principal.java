    private void jMenuItem11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem11ActionPerformed
        // TODO add your handling code here:
        if (ObjProcesamiento.getimg() != null) {
            ObjProcesamiento.resizeimage(120, 120);
            BufferedImage img = ObjProcesamiento.getimg();
            jLabel1.setIcon(new ImageIcon(img));
            this.setTitle("Imagen Pixeles: " + ObjProcesamiento.size());
        }
        if (ObjProcesamiento.getseg() != null) {
            ObjProcesamiento.resizeseg(120, 120);
        }
    }//GEN-LAST:event_jMenuItem11ActionPerformed

    private void jMenuItem12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem12ActionPerformed
        // TODO add your handling code here:
        int ancho = Integer.parseInt(JOptionPane.showInputDialog("Introduzca el ancho :"));
        int alto = Integer.parseInt(JOptionPane.showInputDialog("Introduzca el alto :"));
        if (ObjProcesamiento.getimg() != null) {
            ObjProcesamiento.resizeimage(ancho, alto);
            BufferedImage img = ObjProcesamiento.getimg();
            jLabel1.setIcon(new ImageIcon(img));
            this.setTitle("Imagen Pixeles: " + ObjProcesamiento.size());
        }
        if (ObjProcesamiento.getseg() != null) {
            ObjProcesamiento.resizeseg(ancho, alto);
        }
    }//GEN-LAST:event_jMenuItem12ActionPerformed

