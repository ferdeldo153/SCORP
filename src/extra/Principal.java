    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.scorp", "scorp");
        file.setFileFilter(filtro);
        file.showSaveDialog(this);
        File guarda = file.getSelectedFile();
        if (guarda != null) {
            BufferedImage img = ObjProcesamiento.escalaSegmentada();
            Files a = new Files();
            String name = JOptionPane.showInputDialog("Nombre del objeto :");
            a.save(guarda, ObjProcesamiento.binary(img, name, "1", "0"));
        }
    }//GEN-LAST:event_jMenuItem6ActionPerformed

